#Tupla (INMUTABLES)
datos=(1,5,9,8,11,88,55)
#Acceder al primer elemnto de la tupla
print(datos[0])
#Acceder al ultimo elemento de la tupla
print(datos[-1])

#Operacion avanzada con las tuplas
#Concatenacion de tuplas
datos2=(6,8,9)
datosFusionado=datos+datos2
print(datosFusionado)
#Longitud de una tupla
print(len(datosFusionado))